#include "Whelp.h"

/****************************************************************************/
/* Methods																	*/
/****************************************************************************/
uint8_t attack(Whelp *this)
{
	return this->data.strength;
}

/****************************************************************************/
/* Constructor																*/
/****************************************************************************/
Constructor(Whelp)
{
	this->data.strength = 3;
	this->attack = &attack;
}
