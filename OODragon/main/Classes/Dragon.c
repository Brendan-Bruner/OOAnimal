#include "Dragon.h"

/****************************************************************************/
/* Methods																	*/
/****************************************************************************/
uint8_t attack(Dragon *this)
{
	return this->data.strength;
}

/****************************************************************************/
/* Constructor																*/
/****************************************************************************/
Constructor(Dragon)
{
	this->data.strength = 3;
	this->method.attack = &attack;
}
